    <p>För att delta måste korrekt information anges (namn, adress, telefonnummer, e-postadress, födelsedatum etc.). Vinnare utses genom lottning bland godkända deltagare senast fyra veckor efter tävlingens slut. Vinnare kontaktas via e-post, post eller telefon. Om en vinnare inte kan nås inom rimlig tid eller har uppgett felaktiga uppgifter, kommer en ny vinnare att dras.</p>

    <h2>Tävlingsvinst</h2>
    <p>Vinnaren får det pris som angivits på tävlingens webbplats, till ett värde av högst 10 000 SEK. Priset överförs via bank, PayPal eller post. Eventuell vinstskatt betalas av vinnaren, om inte annat anges i lag. Nordic Digital Marketing AB förbehåller sig rätten att utbetala motsvarande kontantbelopp i stället för varupriset.</p>

