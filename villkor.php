    <h2>Avtal</h2>
    <p>Denna tävling genomförs av Nordic Digital Marketing AB (nedan kallad "arrangören"). Deltagande är helt kostnadsfritt och utan krav på köp av produkter eller tjänster som eventuellt marknadsförs i samband med tävlingen eller via länkar till externa webbplatser. Genom att delta i tävlingen godkänner deltagaren dessa tävlingsvillkor samt arrangörens beslut, som är slutgiltiga. Arrangören ansvarar inte för innehållet på externa webbplatser som tävlingen eventuellt länkar till.</p>

    <h2>Behörighet</h2>
    <p>Tävlingen är öppen för personer som är folkbokförda i Sverige, Norge, Danmark eller Finland och som fyllt 18 år. Anställda hos Nordic Digital Marketing AB, deras närmaste familjemedlemmar eller personer i samma hushåll får inte delta. Endast ett (1) giltigt deltagande per person räknas under tävlingsperioden. Tävlingen avslutas den 31 december 2025. Deltagande sker uteslutande via tävlingens webbplats.</p>

